class UserBroker {
  final String uid;
  final String email;
  final String? displayName;
  final String? photoURL;
  const UserBroker({
    required this.uid,
    required this.email,
    this.displayName,
    this.photoURL,
  });

  Uri? get photoURI => this.photoURL == null ? null : Uri.tryParse(this.photoURL!);
}
